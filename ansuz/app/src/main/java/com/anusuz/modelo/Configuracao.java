package com.anusuz.modelo;

public class Configuracao {

    private String confidence;
    private String distance;
    private String frame_width;
    private String local;
    private String max_disappear;
    private String max_distance;
    private String speed_limit;
    private String track_object;

    public String getConfidence() {
        return confidence;
    }

    public void setConfidence(String confidence) {
        this.confidence = confidence;
    }

    public String getDistance() {
        return distance;
    }

    public void setDistance(String distance) {
        this.distance = distance;
    }

    public String getFrame_width() {
        return frame_width;
    }

    public void setFrame_width(String frame_width) {
        this.frame_width = frame_width;
    }

    public String getLocal() {
        return local;
    }

    public void setLocal(String local) {
        this.local = local;
    }

    public String getMax_disappear() {
        return max_disappear;
    }

    public void setMax_disappear(String max_disappear) {
        this.max_disappear = max_disappear;
    }

    public String getMax_distance() {
        return max_distance;
    }

    public void setMax_distance(String max_distance) {
        this.max_distance = max_distance;
    }

    public String getSpeed_limit() {
        return speed_limit;
    }

    public void setSpeed_limit(String speed_limit) {
        this.speed_limit = speed_limit;
    }

    public String getTrack_object() {
        return track_object;
    }

    public void setTrack_object(String track_object) {
        this.track_object = track_object;
    }

}
